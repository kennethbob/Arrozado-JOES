            <p>Values Education:</p>
               <p id = "catSub"><strong>What is Values-based Education?</strong></p>
               <li id = "description">A values-based school seeks to promote an educational philosophy based on valuing self, others and the environment through, the consideration of an ethical values vocabulary (principles that guide behaviour), as the basis of good educational practice. It encourages adults to model values and to give time for reflective practices that empowers individuals to be effective learners and good citizens. </li>
         </fieldset></br>
         <fieldset class = "mySubFS">
            <p>Values Education</p>
               <li id = "description">Values education is a term used to name several things, and there is much academic controversy surrounding it. Some regard it as all aspects of the process by which teachers (and other adults) transmit values to pupils. Others see it as an activity that can take place in any organisation during which people are assisted by others, who may be older, in a position of authority or are more experienced, to make explicit those values underlying their own behaviour, to assess the effectiveness of these values and associated behaviour for their own and others' long term well-being and to reflect on and acquire other values and behaviour which they recognise as being more effective for long term well-being of self and others.</li></br>
               <li id = "description">This means that values education can take place at home, as well as in schools, colleges, universities, offender's institutions and voluntary youth organisations. There are two main approaches to values education, some see it as inculcating or transmitting a set of values which often come from societal or religious rules or cultural ethics while others see it as a type of Socratic dialogue[2] where people are gradually brought to their own realisation of what is good behaviour for themselves and their community.</li>
         </fieldset>
         <fieldset class = "mySubFS">
            <p>About Values-based Education</p>
               <li id = "description">Values-based Education is a great success and is one of the fastest growing approaches in the world, endorsed by teachers, school leaders and governments. This is because it is based on the soundest principles of pedagogy, educational philosophy, brain research and common sense. It helps pupils to develop holistically, nurturing a secure sense of self, respect for self and others and supports the raising of academic standards.</li>
